package fr.loria.k.revisor.engine.revisorPCSFC.console.exceptions;

public class MissingEnumModalityException extends Exception {

	public MissingEnumModalityException() {
	}

	public MissingEnumModalityException(String message) {
		super(message);
	}

	public MissingEnumModalityException(Throwable cause) {
		super(cause);
	}

	public MissingEnumModalityException(String message, Throwable cause) {
		super(message, cause);
	}

	public MissingEnumModalityException(String message, Throwable cause, boolean enableSuppression,
	boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
