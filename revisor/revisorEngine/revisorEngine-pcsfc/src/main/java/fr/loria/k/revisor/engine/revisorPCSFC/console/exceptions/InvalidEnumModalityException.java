package fr.loria.k.revisor.engine.revisorPCSFC.console.exceptions;

public class InvalidEnumModalityException extends Exception {

	public InvalidEnumModalityException() {
	}

	public InvalidEnumModalityException(String message) {
		super(message);
	}

	public InvalidEnumModalityException(Throwable cause) {
		super(cause);
	}

	public InvalidEnumModalityException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidEnumModalityException(String message, Throwable cause, boolean enableSuppression,
	boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
